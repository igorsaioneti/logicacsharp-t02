//Declarando constantes 

const int valor1 = 10;
const int valor2 = 5;

//Soma
int soma = valor1 + valor2;
Console.WriteLine($"Soma entre os dois valores: " + soma);

//Subtração
int subtracao = valor1 - valor2;
Console.WriteLine($"Subtração entre os dois valores: " + subtracao);

//Multiplicação
int multiplicacao = valor1 * valor2;
Console.WriteLine($"Multiplicação entre os dois valores: " + multiplicacao);

//Divisão
int divisao = valor1 / valor2;
Console.WriteLine($"Divisão entre os dois valores: " + divisao);

//Resto da divisão
int restDivisao = valor1 % valor2;
Console.WriteLine($"Resto da divisão entre os dois valores: " +  restDivisao);

//Potenciação
double potencia = Math.Pow(valor1, valor2);
Console.WriteLine($"Potenciação entre os valores: {valor1} elevado a {valor2} = {potencia} ");

int resultado1 = 3 + 5 * 2;
Console.WriteLine($"Resultado 1 (sem parênteses): {resultado1}");

int resultado2 = (3 + 5) * 2;
Console.WriteLine($"Resultado 2 (com parênteses): {resultado2}");

int resultado3 = 8 / 4 + 3;
Console.WriteLine($"Resultado 3 (sem parênteses): {resultado3}");

double resultado4 = 8.0 / (4 + 3);
Console.WriteLine($"Resultado 4 (com parênteses): {resultado4}");






